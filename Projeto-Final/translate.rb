Pra parar de usar fralda o bebê tem que não mamar de noite, andar bem sozinho e saber comunicar, o que dá mais de 22 meses. Vamos arredondar pra 2 anos (24 meses).
Um bebê usa em média nos dois primeiros meses 8 fraldas por dia. 
Do terceiro ao nono mês essa média cai pra 6.

- Até 2 meses - 240 fraldas por mês - total: 480 fraldas tamanho P
- De 3 a 8 meses - 180 fraldas por mês - total: 1080 fraldas tamanho M
- De 9 a 18 meses - 150 fraldas por mês - total: 1500 fraldas tamanho G
- De 18 a 24 meses - 150 fraldas por mês - total: 1050 fraldas tamanho EG